    private void jButtonUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUpdateMouseClicked
             model.setValueAt(jTextFieldID.getText(), jTableProf.getSelectedRow(), 0);
             model.setValueAt(jTextFieldName.getText(), jTableProf.getSelectedRow(), 1);
             model.setValueAt(jTextFieldMobile.getText(), jTableProf.getSelectedRow(), 2);
             if(professors.contains(prof)){
                int index = professors.indexOf(prof);
                professors.set(index, prof);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Professor is Not Replaced!");
            }
    }//GEN-LAST:event_jButtonUpdateMouseClicked

