    private void jButtonAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAddMouseClicked
        if(!jTextFieldID.getText().isEmpty() && !jTextFieldName.getText().isEmpty() && !jTextFieldMobile.getText().isEmpty())
        {
            
            model.insertRow(model.getRowCount(), new String[]{jTextFieldID.getText(),jTextFieldName.getText(),jTextFieldMobile.getText()});
            connection.addProfessor(this, jTextFieldID.getText(), jTextFieldName.getText(), jTextFieldMobile.getText());
            professors.add(prof);
            jTextFieldID.setText("");
            jTextFieldName.setText("");
            jTextFieldMobile.setText("");
                    
        }
        else{
            JOptionPane.showMessageDialog(this, "Please\nEnter valid data!");
        }
        
        
    }//GEN-LAST:event_jButtonAddMouseClicked

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
       model.removeRow(jTableProf.getSelectedRow());
       jTextFieldID.setText("");
       jTextFieldName.setText("");
       jTextFieldMobile.setText("");
       System.out.println(prof.getId()+"    "+prof.getProfName()+"  "+ prof.getMobNo());
       if(professors.contains(prof)){
           int index = professors.indexOf(prof);
           professors.remove(prof);
       }
       else
       {
           JOptionPane.showMessageDialog(this, "Professor is Not Deleted!");
       }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jTableProfMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProfMouseClicked
        jTextFieldID.setText((String)model.getValueAt(jTableProf.getSelectedRow(), 0));
        jTextFieldName.setText((String)model.getValueAt(jTableProf.getSelectedRow(), 1));
        jTextFieldMobile.setText((String)model.getValueAt(jTableProf.getSelectedRow(), 2));
        prof.setId((String)model.getValueAt(jTableProf.getSelectedRow(), 0));
        prof.setProfName((String)model.getValueAt(jTableProf.getSelectedRow(), 1));
        prof.setMobNo((String)model.getValueAt(jTableProf.getSelectedRow(), 2));
    }//GEN-LAST:event_jTableProfMouseClicked

    
